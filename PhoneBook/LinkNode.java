package PhoneBook;

// One Node of linkedList. Four data values.
public class LinkNode {

    public LinkNode next;

    public String name;

    public String phone;

    public String address;

    public String city;

    public LinkNode(String name, String phone, String address, String city){

        this.name = name;
        this.phone = phone;
        this.address = address;
        this.city = city;


    }




}

